"""
DEFINING DICTIONARY

"""

"""
A dictionary is a collection of key-value pairs.
Each key is unique and values can be of any type.

Syntax:
dict_name ={key1:value1,key2:value2.....}
"""

#1. Empty dictionary
empty_dict ={}
print(empty_dict)

#2. Simple Dictionary
student ={'name':'Anil','age':21,'grade':'A'};print(student)

#3. Product Dictionary
product ={'Laptop':50000,'Mouse':700}
print(product)


#4. Marks Dictionary
marks ={'Math':85,'Science':92}
print(marks)

#5. Boolean Values
status={'LoggedIn':True,'verified':False};print(status)

#6. List as a value
hobbies ={'Rahul':['Gaming','Reading']};print(hobbies)

#7. Nested Dictionaries
employees ={'HR':{'COUNT':5},'IT':{'count':10}}
print(employees)

#8. Using int key
room ={1:'occupied',2:'Vacant'}
print(room)

#9. Mix Values
data ={'name':'Reema','score':88,'pass':True}
print(data)

# 10. Duplicate keys (last one overrides)
test = {'a': 10, 'a': 20}
print(test)

#11. Using Tuple as key
location ={(10.5,20.6):"Point A"};print(location)

#12. Mobile Brand Info
mobile ={'brand':'Samsung','model':'A52','price':25000}
print(mobile)


#13. Game Info
game ={'name':'Chess','players':2};print(game)


#14. USE THE STRINGS AS KEYS
info ={'Name':'Amit','City':'Delhi'}
print(info)


#15. Language Use
lang ={'Python':'Web','C++':'Game Dev'};print(lang)


#16. College Details
college ={'name':'ABC University','rank':5};print(college)


#17. Car Info
car ={'make':'Honda','year':2020};print(car)


#18. City Population
population ={'Mumbai':20000000,'Pune':70000000};print(population)

#19.Sports and coach
sports ={'Cricket':'Ravi','Hockey':'Surjit'};print(sports)

#20. Food Calories
food ={'Apple':95,'Banana':105};print(food)


#21. Srudent Marksheet

marksheet ={'Roll':101,'Name':'Meena','Marks':85}
print(marksheet)


#22.  Devices and Quantity
inventory ={'Laptop':10,'Monitor':5}
print(inventory)

#23. Profiles with address
profile ={'name':'Pooja','address':{'city':'Indore','pincode':452001}}
print(profile)


#24. Social Media stats
stats ={'likes':230,'comments':45}
print(stats)


#25. Festival Dates
festivals ={'Holi':'March','Diwali':'November'}
print(festivals)




